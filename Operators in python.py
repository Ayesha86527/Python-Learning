#Types Of Operators

#Arithmatic Operators
a=5
b=8
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b)
print(a**b)

#Relational/Comparison Operators
a=10
b=20
print(a==b)
print(a!=b) #not equal to sign
print(a>=b)
print(a<=b)
print(a>b)
print(a<b)

#Assignment Operators
num=16
num+=10 #Above expression ko short mein likne ka tariqa
print(num)

#Same goes for other signs

#Logical Operators(and,not and or)
print(not False)
print(not True)

a=50
b=30
print(not(a<b))

val1=True
val2=False
print("and operator:",val1 and val2)

print("or operator:",val1 or val2)

print("or operator:",a==b or a>b)



